# 1이상 100이하 리스트 만들기

number_list = list(range(1, 101, 1))
print(number_list)

# 1이상 100이하 짝수 리스트 만들기

even_list = list(range(2, 102, 2))
print(even_list)

# 1이상 100이하 홀수 리스트 만들기

odd_list = list(range(1, 101, 2))
print(odd_list)

# -100보다 크고 0이하 홀수 리스트 만들기

minus_list = list(range(-99, 1, 1))
print(minus_list)